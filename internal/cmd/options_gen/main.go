package main

import (
	"log"
	"os"
	"text/template"

	"github.com/Masterminds/sprig"
	"github.com/Xuanwo/storage/define"
)

func main() {
	const filePath = "options.go"

	f, err := os.Create(filePath)
	if err != nil {
		log.Fatal(err)
	}
	defer f.Close()

	err = tmpl.Execute(f, struct {
		Data map[string]string
	}{
		define.AvailableOptions,
	})
	if err != nil {
		log.Fatal(err)
	}
}

var tmpl = template.Must(template.New("").Funcs(sprig.HermeticTxtFuncMap()).Parse(`// Code generated by go generate internal/cmd/options_gen; DO NOT EDIT.
package define

{{- range $k, $v := .Data }}
// With{{ $k | camelcase }} will apply {{ $k }} value to Options
func With{{ $k | camelcase }}(v {{ $v }}) *Option {
	return &Option{
		Key: "{{ $k }}",
		Value: v,
	}
}
{{- end }}
`))
